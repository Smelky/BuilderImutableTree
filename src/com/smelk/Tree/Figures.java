package com.smelk.Tree;

abstract class Figures {
    String name;
    int corners;
    int radius;
    int perimeter;
    int area;
    int side;
    int height;
    int bulk;

    public String getName() {
        return name;
    }

    public int getCorners() {
        return corners;
    }

    public int getRadius() {
        return radius;
    }

    public int getPerimeter() {
        return perimeter;
    }

    public int getArea() {
        return area;
    }

    public int getSide() {
        return side;
    }

    public int getHeight() {
        return height;
    }

    public int getBulk() {
        return bulk;
    }
}
